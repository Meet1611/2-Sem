import java.util.*;

public class SumofTwoSc {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.print("Enter First Number : ");
        int n1 = sc.nextInt();

        System.out.print("Enter Second Number : ");
        int n2 = sc.nextInt();

        System.out.println(n1 + n2);
    }
}
